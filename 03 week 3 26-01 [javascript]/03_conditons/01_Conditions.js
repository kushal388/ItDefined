// switch  note if we dont use break it will print all after  the case matched!!
// break : come out of the block


let number =3;

switch(number){
    case 1: 
            console.log("one");
             break;
    case 2: 
            console.log("two");
             break;
    case 3: 
            console.log("three");
            break;
    case 4: 
            console.log("four");
             break;
    default:
            console.log("not a given number");
     
                      
    
}